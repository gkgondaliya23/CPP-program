// unary oprator (increment)
#include<iostream>
using namespace std;
int main()
{
    int a=10;
    cout<<"a: "<<a++;
    cout<<"\na: "<<++a;
    cout<<"\na: "<<a++;
    cout<<"\na: "<<++a;
    cout<<"\na: "<<++a;
    cout<<"\na: "<<a++;
    cout<<"\na: "<<a++;
    cout<<"\na: "<<++a;
    cout<<"\na: "<<++a;
    cout<<"\na: "<<++a;
    return 0;
}