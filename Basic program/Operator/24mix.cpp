// unary oprator (mix inc / dec)
#include<iostream>
using namespace std;
int main()
{
    int a=30;
    cout<<"a: "<<a++;
    cout<<"\na: "<<--a;
    cout<<"\na: "<<a++;
    cout<<"\na: "<<++a;
    cout<<"\na: "<<--a;
    cout<<"\na: "<<a--;
    cout<<"\na: "<<a++;
    cout<<"\na: "<<--a;
    cout<<"\na: "<<a++;
    cout<<"\na: "<<++a;
    return 0;
}